package pkg1;

public class Fibonacciwhile {
	public static void main(String[] args) {
		int num=10;
		int a=0;
		int b=1;
		int temp=1;
		while(temp<=num) {
			
			System.out.println("fibonacci series is:  "+a);
			temp=a+b;
			a=b;
			b=temp;
			temp=a;
			temp++;
			
		}
		System.out.println();
		
	}

}
