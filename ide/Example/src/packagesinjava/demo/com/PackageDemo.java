
//user defined package
package packagesinjava.demo.com;
import java.util.*;
public class PackageDemo {
/*there are two types of packages in java
 * inbuilt
 * user defined
 */
	public static void main(String[] args) {
		//inbuilt package
		Scanner sc=new Scanner(System.in);//System.in is a constructor's passed parameter
		int a=sc.nextInt();
		System.out.println(a);
	}
}
